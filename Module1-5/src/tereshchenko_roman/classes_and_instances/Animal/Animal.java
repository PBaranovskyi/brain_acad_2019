package com.brainacad.classes_and_instances.Animal;

public class Animal {

    private String name;

    public void setName(String n) {
        name = n;
    }

    public String getName() {
        return name;
    }

    private int legs;

    public void setLegs(int a) {
        legs = a;
    }

    public int getLegs() {
        return legs;
    }

    private boolean mamal;

    public void setMamal(boolean b) {
        mamal = b;
    }

    public boolean getMamal() {
        return mamal;
    }
}

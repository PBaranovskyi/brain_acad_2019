public class Car {
    private int maxSpeed;
    private int mileage;

    public Car(int maxSpeed, int mileage) {
        this.maxSpeed = maxSpeed;
        this.mileage = mileage;
    }

    public int getMaxSpeed() {
        return maxSpeed;
    }

    public int getMileage() {
        return mileage;
    }


}

package mykhailo_kaloshyn.mainacad.lab1_4;

public class Lab1_4_4 {
    public static void main(String[] args) {
        int a = 15;
        System.out.println("a = " + a);
        System.out.println("++a = " + ++a);
        System.out.println("a++ = " + a++);
        System.out.println("--a = " + --a);
        System.out.println("a-- = " + a--);
        System.out.println("finally a = " + a);
    }
}

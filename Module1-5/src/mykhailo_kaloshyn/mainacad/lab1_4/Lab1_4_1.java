package mykhailo_kaloshyn.mainacad.lab1_4;

public class Lab1_4_1 {
    public static void main(String[] args) {
        byte newByte = 5;
        short newShort = 11751;
        int newInt = 350755;
        long newLong = 155_400_000_000L;
        float newFloat = 3.14F;
        double newDouble = 11.8;
        boolean newBoolean = true;
        char newChar = '\u0fff';
        System.out.println("newByte = " + newByte);
        System.out.println("newShort = " + newShort);
        System.out.println("newInt = " + newInt);
        System.out.println("newLong = " + newLong);
        System.out.println("newFloat = " + newFloat);
        System.out.println("newDouble = " + newDouble);
        System.out.println("newBoolean = " + newBoolean);
        System.out.println("newChar = " + newChar);
    }
}

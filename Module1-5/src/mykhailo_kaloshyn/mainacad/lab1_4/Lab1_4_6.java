package mykhailo_kaloshyn.mainacad.lab1_4;

public class Lab1_4_6 {
    public static void main(String[] args) {
        int a = 10, b = 12;
        System.out.println(a + " | " + b + " = " + (a | b));
        System.out.println(a + " & " + b + " = " + (a & b));
        System.out.println(a + " ^ " + b + " = " + (a ^ b));
        System.out.println("~" + a + " = " + (~a));
        System.out.println("~" + b + " = " + (~b));
    }
}

package mykhailo_kaloshyn.mainacad.lab1_4;

public class Lab1_4_8 {
    public static void main(String[] args) {
        long newLong = 55_000_000_000L;
        int newLongToInt = (int) newLong;
        System.out.println("Long = " + newLong);
        System.out.println("LongToInt = " + newLongToInt + '\n');
        char newChar = 'w';
        int newCharToInt = (int) newChar;
        System.out.println("Char = " + newChar);
        System.out.println("CharToInt = " + newCharToInt);
    }
}

package mainclasses.classwork2;

public class Animal {
    private String name;
    private int legs;
    private boolean mamal;

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setLegs(int legs) {
        this.legs = legs;
    }

    public int getLegs() {
        return legs;
    }

    public void setMamal(boolean mamal) {
        this.mamal = mamal;
    }

    public boolean getMamal() {
        return mamal;
    }
}

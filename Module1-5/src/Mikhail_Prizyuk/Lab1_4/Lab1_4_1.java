package Mikhail_Prizyuk.Lab1_4;

public class Lab1_4_1 {
    public static void main(String[] args) {
        byte myByteVal = 10;
        short myShortVal = 100;
        int myIntVal = 1000;
        long myLongVal = 123456789L;

        System.out.println("myByteVal =" + myByteVal);
        System.out.println("myShortVal =" + myShortVal);
        System.out.println("myIntVal =" + myIntVal);
        System.out.println("myLongVal =" + myLongVal);

        float myFloatVal = 3.14F;
        double myDoublVal = 56.70D;

        System.out.println("myFloatVal =" + myFloatVal);
        System.out.println("myDoublVal =" + myDoublVal);

        char myChar = 'A';
        char myForward = '\n';
        char myUni = '\u00a9';

        System.out.println("myChar =" + myChar);
        System.out.println("myForward =" + myForward);
        System.out.println("myUni =" + myUni);

        boolean b1 = false;
        boolean b2 = true;

        System.out.println("boolean b1 =" + b1);
        System.out.println("boolean b2 =" + b2);


    }
}
